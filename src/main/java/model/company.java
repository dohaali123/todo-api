package com.example.todo.model;

public class Company {
    private Long id;
    private String name;

    // Constructors
    public Company() {
    }

    public Company(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
